n = int(input())

dp = [list([0] * (3)) for _ in range(n + 1)]

dp[1][0] = 1
dp[1][1] = 1
dp[1][2] = 1

# 아무것도 선택하지 않은 경우, 왼쪽만 선택한 경우, 오른쪽만 선택한 경우를 
# 점화식으로 구성한다
for i in range(2, n + 1):
  dp[i][0] = (dp[i-1][0] + dp[i-1][1] + dp[i-1][2]) % 9901
  dp[i][1] = (dp[i-1][0] + dp[i-1][2]) % 9901
  dp[i][2] = (dp[i-1][0] + dp[i-1][1]) % 9901

print(sum(dp[n]) % 9901)